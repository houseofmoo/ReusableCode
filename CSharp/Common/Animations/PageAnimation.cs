
namespace Common.Animations
{
    /// <summary>
    /// The page animations avaiable
    /// </summary>
    public enum PageAnimation
    {
        /// <summary>
        /// No animation
        /// </summary>
        None = 0,

        /// <summary>
        /// Fade in animation
        /// </summary>
        FadeIn = 1,

        /// <summary>
        /// Fade out animation
        /// </summary>
        FadeOut = 2,

        /// <summary>
        /// Slide in from right animation
        /// </summary>
        SlideInFromRight = 3,

        /// <summary>
        /// Slide out to left animation
        /// </summary>
        SlideOutToLeft = 4,

        /// <summary>
        /// Slide in from right while fading in
        /// </summary>
        SlideAndFadeInFromRight = 5,

        /// <summary>
        /// Slide out to left while fading out
        /// </summary>
        SlideAndFadeOutToLeft = 6,
    }
}
